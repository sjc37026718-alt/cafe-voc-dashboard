def load_feedbacks():
    if not os.path.exists(DATA_PATH):
        save_feedbacks(INITIAL_DATA)
    with open(DATA_PATH, "r", encoding="utf-8") as f:
        return json.load(f)

def save_feedbacks(data):
    with open(DATA_PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def add_feedback(날짜, 경로, 별점, 내용, 유형="미분류"):
    data = load_feedbacks()
    new_id = max(d["id"] for d in data) + 1
    data.append({
        "id": new_id,
        "날짜": str(날짜),
        "경로": 경로,
        "별점": 별점,
        "내용": 내용,
        "유형": 유형,
    })
    save_feedbacks(data)
    return new_id
